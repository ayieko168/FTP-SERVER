
import socket
import json
import os


DEBUG = True
ROOT_DIR = f"{os.sep}".join(os.path.abspath(__file__).split(os.sep)[:-2])


def get_local_ip():

    s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    s.connect(("8.8.8.8", 80))
    local_ip = s.getsockname()[0]
    s.close()
    if DEBUG: print(local_ip)

    return str(local_ip)


def get_config_variables():

    with open(os.path.join(ROOT_DIR, 'bin/config.json'), 'r') as fo:
        return json.load(fo)


def dump_new_settings(config_json):
    with open(os.path.join(ROOT_DIR, 'bin/config.json'), 'w') as fo:
        json.dump(config_json, fo, indent=2)







































































































































































































































